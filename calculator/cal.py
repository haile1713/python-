num1= int(input("input ur first number :"))
num2= int(input("input ur second number :"))
operator = input("Specify the math operation here (+, -, *, /): ")

if operator == "+":
    print (num1+num2)
elif operator == "-":
    print (num1-num2)
elif operator == "*":
    print (num1*num2)
elif operator == "/":
    print (num1/num2)

